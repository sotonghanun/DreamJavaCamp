package dreamschool.cscourse.chapter06;

public class Overloading {
	void addition(int num1, int num2) {
		System.out.println(num1 + num2);
	}
	
	void addition(double num1, double num2) {
		System.out.println(num1 + num2);
	}
	
	void addition(String num1, String num2) {
		System.out.println(num1 + num2);
	}

}
